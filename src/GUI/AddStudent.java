    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String name = jTextField1.getText();
            String surname = jTextField3.getText();
            int genderSelection = jComboBox1.getSelectedIndex();
            char gender = 'M';
            if (genderSelection == 1) {
                gender = 'F';
            }
            LocalDate selectedDate = datePicker1.getDate();
            String nation = convertNation(jComboBox2.getSelectedItem().toString());
            String department = convertDepartment(jComboBox3.getSelectedItem().toString());
           
            boolean result = currentHelper.writeToDatabase((this.lastID + 1) + "", name, surname, gender + "", selectedDate, nation, department);
            if (result) {
                JOptionPane.showMessageDialog(this, "Record Successfully added!", "Success!", JOptionPane.INFORMATION_MESSAGE);
            }
            this.dbChanged = true;
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Unable to reach database!", "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

