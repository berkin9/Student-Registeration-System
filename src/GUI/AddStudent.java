    private Date convertSelectedDate(LocalDate date) {
        ZoneId defaultZoneId = ZoneId.systemDefault();
        return Date.from(date.atStartOfDay(defaultZoneId).toInstant());
    }

    private void fillListElements() {
        HashMap<String, String> nations = this.currentHelper.countries;
        List<String> countryList = new ArrayList<>(nations.values());
        Collections.sort(countryList);
        for (int i = 0; i < countryList.size(); i++) {
            jComboBox2.addItem(countryList.get(i));
        }
        HashMap<String, String> departments = this.currentHelper.depts;
        List<String> deptlist = new ArrayList<>(departments.values());
        Collections.sort(deptlist);
        for (int i = 0; i < deptlist.size(); i++) {
            jComboBox3.addItem(deptlist.get(i));
        }
    }

    private String convertNation(String nation) {
        for (Entry<String, String> entry : currentHelper.countries.entrySet()) {
            if (entry.getValue().equals(nation)) {
                return entry.getKey();
            }
        }
        return null;
    }

    private String convertDepartment(String dept) {
        for (Entry<String, String> entry : currentHelper.depts.entrySet()) {
            if (entry.getValue().equals(dept)) {
                return entry.getKey();
            }
        }
        return null;
    }

